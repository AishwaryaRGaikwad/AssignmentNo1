
public class PrintNoPattern {

	public static void main(String[] args) {

        int i,j,num=1;
        for(i=0;i<=3;i++)
        {
        	for(j=0;j<=3;j++)
        	{
        		System.out.print(num);
        	}
        	System.out.println(" ");
        	num++;
        }
		}
	
	}


